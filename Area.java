public class Area {
    public static void main(String[] args){
        double len =10;
        double bre =20;
        double rectArea = len*bre;
        System.out.println("The Area of Rectangle is "+rectArea);
        double base = 10;
        double height = 20;
        double triArea = 0.5 * base * height;
        System.out.println("Area of Triangle is "+triArea);
    }
    
}
